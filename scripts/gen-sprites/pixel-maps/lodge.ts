// as light, not matter; the one outline-rule exception, see
// assets/STYLE.md). BL-10: mechanically recolored from the old cool
// palette (structure/frames unchanged) — 1/2/3/k kept their chars (palette
// swap alone re-tones them warm); 6->t (dark wood), 0->D, 9->C, 5->B
// (spark teal ramp -> pacifier-blue ramp, palest to darkest).

